    private void isiComboBoxAlbum() {
        try (Connection con = Koneksi.getConnection();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT * FROM album")) {
            while (rs.next()) {
                cbAlbum.addItem(rs.getString("nama_album") + "(Rp" + rs.getInt("harga") + ")");
                listKodeAlbum.add(rs.getString("kode_album"));
                listHargaAlbum.add(rs.getInt("harga"));
            }
        } catch (SQLException ex) {System.err.println(ex.getMessage());}
    }
    
    public void siapkanDataEdit(int idPesanan){
        this.idEdit = idPesanan;
        setTitle("Ubah Rekap Pesanan PO");
        jLabel5.setText("FORM UBAH ORDER");
        btnSimpanOrder.setText("Simpan Perubahan");
        
        String sql = "SELECT p.nama_pembeli, p.no_hp, p.kode_album, p.jumlah FROM pesanan p WHERE p.id_pesanan = ?";
        try (Connection con = Koneksi.getConnection();
                PreparedStatement ps = con.prepareStatement(sql)){
            ps.setInt(1, idPesanan);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                txtNama.setText(rs.getString("nama_pembeli"));
                txtHp.setText(rs.getString("no_hp"));
                txtJumlah.setText(String.valueOf(rs.getInt("jumlah")));
                String kodeDb = rs.getString("kode_album");
                int indexAlbum = listKodeAlbum.indexOf(kodeDb);
                if (indexAlbum != -1){
                    cbAlbum.setSelectedIndex(indexAlbum);
                }
            }
        } catch (SQLException ex){
            System.err.println("Gagal memuat data lama: " + ex.getMessage());
        } 
    }

