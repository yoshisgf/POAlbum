    private void btnSimpanOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanOrderActionPerformed
        if (cbAlbum.getSelectedIndex() == -1 || txtJumlah.getText().isEmpty() || txtNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Form harus terisi!");
            return;
        }
        
        int index = cbAlbum.getSelectedIndex();
        String kodeAlbum = listKodeAlbum.get(index);
        int hargaAlbum = listHargaAlbum.get(index);
        int jmlBeli = Integer.parseInt(txtJumlah.getText());
        int totalBayar = hargaAlbum * jmlBeli;
        
        String sql;
        if (idEdit == -1){
            sql = "INSERT INTO pesanan (nama_pembeli, no_hp, kode_album, jumlah, total) VALUES (?, ?, ?, ?, ?)";
        } else{
            sql = "UPDATE pesanan SET nama_pembeli=?, no_hp=?, kode_album=?, jumlah=?, total=? WHERE id_pesanan=?";
        }
        try (Connection con = Koneksi.getConnection();
            PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, txtNama.getText());
            ps.setString(2, txtHp.getText());
            ps.setString(3, kodeAlbum);
            ps.setInt(4, jmlBeli);
            ps.setInt(5, totalBayar);
            if (idEdit != -1){
                ps.setInt(6, idEdit);
            }
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Pesanan Telah Berhasil!\nTotal: Rp " + totalBayar);
            parent.loadData("");
            dispose();
        } catch (SQLException ex) {JOptionPane.showMessageDialog(this, ex.getMessage());}
    }//GEN-LAST:event_btnSimpanOrderActionPerformed

